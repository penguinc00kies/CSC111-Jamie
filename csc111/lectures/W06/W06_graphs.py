def complete_graph(n: int) -> Graph:
    """Return a graph of n vertices where all pairs of vertices are adjacent.

    The vertex items are the numbers 0 through n - 1, inclusive.

    Preconditions:
        - n >= 0
    """
    graph = Graph()
    for i in range(0, n):
        graph.add_vertex(i)
        for j in range(0, i):
            graph.add_edge(i, j)

    return graph